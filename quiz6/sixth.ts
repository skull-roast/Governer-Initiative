// Storing a name with whitespace characters
const nameWithWhitespace: string = "\t  \n John Doe \t\n";

// Printing the name with surrounding whitespace
console.log("Name with whitespace:");
console.log(nameWithWhitespace);

// Stripping the whitespace
const strippedName: string = nameWithWhitespace.trim();

// Printing the stripped name
console.log("\nName after stripping whitespace:");
console.log(strippedName);



